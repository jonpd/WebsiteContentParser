package uk.jonathanpeterdavies.websitecontentparser;

public class AverageSiteStats {

	private double averageComplexity;
	private double averageReferencesRIBA;
	private double averageReferencesARB;
	private double averageReferencesArchitects;
	private double averageReferencesPracticeName;
	private double averageReferencesCost;
	private boolean averageGoogleScripted;
	private double averageNumberMainPages;
	private double averageNumberSocialMediaLinks;

	public AverageSiteStats() {

	}

	public double getAverageComplexity() {
		return averageComplexity;
	}

	public void setAverageComplexity(double averageComplexity) {
		this.averageComplexity = averageComplexity;
	}

	public double getAverageReferencesRIBA() {
		return averageReferencesRIBA;
	}

	public void setAverageReferencesRIBA(double averageReferencesRIBA) {
		this.averageReferencesRIBA = averageReferencesRIBA;
	}

	public double getAverageReferencesARB() {
		return averageReferencesARB;
	}

	public void setAverageReferencesARB(double averageReferencesARB) {
		this.averageReferencesARB = averageReferencesARB;
	}

	public double getAverageReferencesArchitects() {
		return averageReferencesArchitects;
	}

	public void setAverageReferencesArchitects(double averageReferencesArchitects) {
		this.averageReferencesArchitects = averageReferencesArchitects;
	}

	public double getAverageReferencesPracticeName() {
		return averageReferencesPracticeName;
	}

	public void setAverageReferencesPracticeName(double averageReferencesPracticeName) {
		this.averageReferencesPracticeName = averageReferencesPracticeName;
	}

	public double getAverageReferencesCost() {
		return averageReferencesCost;
	}

	public void setAverageReferencesCost(double averageReferencesCost) {
		this.averageReferencesCost = averageReferencesCost;
	}

	public boolean isAverageGoogleScripted() {
		return averageGoogleScripted;
	}

	public void setAverageGoogleScripted(boolean averageGoogleScripted) {
		this.averageGoogleScripted = averageGoogleScripted;
	}

	public double getAverageNumberMainPages() {
		return averageNumberMainPages;
	}

	public void setAverageNumberMainPages(double averageNumberMainPages) {
		this.averageNumberMainPages = averageNumberMainPages;
	}

	public double getAverageNumberSocialMediaLinks() {
		return averageNumberSocialMediaLinks;
	}

	public void setAverageNumberSocialMediaLinks(double averageNumberSocialMediaLinks) {
		this.averageNumberSocialMediaLinks = averageNumberSocialMediaLinks;
	}

	
}
